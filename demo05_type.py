#介绍python常见的数据类型
#数字类型
myAge = 20 #整数
myScore = 80.5 #小数
isLogin = True #bool
mySigal = 3+2j #comlex复数
print(myAge,type(myAge))
print(myScore,type(myScore))
print(isLogin,type(isLogin))
print(mySigal,type(mySigal))
#字符串
myName = "john"
myMsg = "sad sad gfdf gfdg"
myAddr = """北京
安徽山东爱仕达按时的
"""
print(myName,type(myName))
print(myMsg,type(myMsg))
print(myAddr,type(myAddr))